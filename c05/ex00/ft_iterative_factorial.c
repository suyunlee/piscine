int	ft_iterative_factorial(int nb)
{
	int	i;
	int	fac;

	i = 1;
	fac = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		fac *= i;
		i++;
	}
	return (fac);
}
