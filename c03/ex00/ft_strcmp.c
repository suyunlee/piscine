int	ft_strcmp(char *s1, char *s2)
{
	int	cmp;

	while (*s1 != 0 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	cmp = (unsigned char)*s1 - (unsigned char)*s2;
	return (cmp);
}
